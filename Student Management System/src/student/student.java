    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            stmt = conn.createStatement();
            
            int id = Integer.parseInt(stdid.getText());
            
            String sql = "SELECT *FROM STUDENT WHERE id = '"+id+"'";
            
            rs = stmt.executeQuery(sql);
            
            if(rs.next()) {
                name.setText(rs.getString("stdName"));
                roll.setText(rs.getString("stdRoll"));
                grade.setText(rs.getString("stdGrade"));
                city.setText(rs.getString("stdCity"));
                classes.setText(String.format("%s", rs.getInt("stdClass")));
                
                
                    
            }
            
        } catch(NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Record Not Founded");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

